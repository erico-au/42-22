int	ft_sqrt(int nb)
{
	int	x;

	x = 0;
	if (nb >= x)
	{
		if (nb == 1)
			return (nb);
		while (x < nb)
		{
			if (x * x == nb)
				return (x);
			else if (x * x > nb)
				return (0);
			x++;
		}
	}
	return (0);
}
